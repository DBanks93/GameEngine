package uk.co.dnbanks.gameengine.utilities;

/**
 * Class that implements 2D Vectors.
 * @author Daniel Banks
 */
public class Vector2D {

    /** X value. */
    public int x;

    /** Y value. */
    public int y;

    /**
     * Creates a 2D vector.
     * @param x x Value
     * @param y y Value
     */
    public Vector2D(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
